def square(side):
  area = side*side
  return area

def rectangle(length,breadth):
  area = length*breadth
  return area

def triangle(base,height):
  area = 0.5*base*height
  return area

def circle(radius):
  area = 3.14*radius*radius
  return area
 
def rhombus(diag1,diag2):
  area = 0.5*diag1*diag2
  return area


